# Программа яка провіряє чи є палиндромом введена фраза

s = input("Enter word: ")

def polindrom(s):
    l = len(s)     # довжина строки
    m = ' ,-?!'    # що забрати і перевести в "" пустоту
    for j in m:
        s = s.replace(j, '')  # забирає пробел, кому, тире, знак питання і знак оклику
    s = s.lower()    # зводить все до малих букв
    for i in range(l//2):
        if s[i] != s[-1-i]:
            return "Не палиндром"
    return "Полиндром"

print(polindrom(s))

